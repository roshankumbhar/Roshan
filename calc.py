""" This my calculator module """


def add(a, b):
    """Addition Module
    :param a:
    :param b:
    :return:
    """
    return a + b


def sub(a, b):
    """Substraction Module
    :rtype: object
    :param a:
    :param b:
    :return:
    """
    return a - b


def mul(a, b):
    """Multiplication Module
    :param a:
    :param b:
    :return:
    """
    return a * b


def div(a, b):
    """Division Module
    :param a:
    :param b:
    :return:
    """
    return a / b
